#!/bin/bash
set -euo pipefail
cd "$(dirname "$0")/.."

echo "=== Generating Simpsons Demo Data ==="
echo "Requires: both emulators running (nerdster on 8080/5001, oneofus on 8081/5002)"
echo ""

OUTPUT=$(python3 bin/chrome_widget_runner.py --headless -t lib/dev/simpsons_demo_generator.dart 2>&1)
echo "$OUTPUT"

PUBLIC_KEYS_JSON=$(echo "$OUTPUT" | awk '/===PUBLIC_KEYS_JSON_START===/{flag=1; next} /===PUBLIC_KEYS_JSON_END===/{flag=0} flag')
PRIVATE_KEYS_JSON=$(echo "$OUTPUT" | awk '/===PRIVATE_KEYS_JSON_START===/{flag=1; next} /===PRIVATE_KEYS_JSON_END===/{flag=0} flag')

if [ -z "$PUBLIC_KEYS_JSON" ]; then
    echo "ERROR: Could not extract public keys — did the test PASS?"
    exit 1
fi

# Pure JSON — consumed by Hablotengo's createSimpsonsContactData.sh.
echo "$PUBLIC_KEYS_JSON" > ../simpsonsPublicKeys.json
echo "Written: ../simpsonsPublicKeys.json"

# JS wrapper — consumed by web pages.
echo "// NOTE TO AI AGENT: Do not edit. Generated by createSimpsonsDemoData.sh." > web/common/data/demoData.js
echo "const demoData = $PUBLIC_KEYS_JSON;" >> web/common/data/demoData.js
echo "Written: web/common/data/demoData.js"

if [ -n "$PRIVATE_KEYS_JSON" ]; then
    # Pure JSON — for importing into the ONE-OF-US.NET identity app only.
    echo "$PRIVATE_KEYS_JSON" > ../simpsonsPrivateKeys.json
    echo "Written: ../simpsonsPrivateKeys.json"
else
    echo "WARNING: Could not extract private keys"
fi
