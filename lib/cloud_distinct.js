function keyToken(dict) {
  const sortedKeys = Object.keys(dict).sort();
  let resultString = "";
  for (const key of sortedKeys) {
    resultString += dict[key];
  }
  return resultString;
}

function getVerbSubject(j) {
  for (var verb of contentVerbs) {
    if (j[verb] != null) {
      return [verb, j[verb]];
    }
  }
  return null;
}

function clouddistinct(input) {
  var out = [];
  var already = new Set();
  for (var j of input) {
    var i = j['I'];
    const [verb, subject] = getVerbSubject(j);
    var key = keyToken(subject);
    if (already.has(key)) continue;
    already.add(key);
    if (verb == 'clear') continue;
    delete j.I;
    delete j.statement;
    delete j.signature;
    delete j.previous;
    out.push(j);
  }
  return out;
}

